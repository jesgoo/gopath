public class ComboxInfo {
	
	private String id;//option的value
	
	private String text;//option显示的值
	
	private boolean isSelected;//是否为选中状态
	
	public ComboxInfo(String id,String text) {
		isSelected = false;
		setId(id);
		setText(text);
	}
	
	public void setId(String id) {
		this.id = id;
	}
	public String getId() {
		return id;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getText() {
		return text;
	}
	public void setSelected(boolean isSelected) {
		this.isSelected = isSelected;
	}
	public boolean isSelected() {
		return isSelected;
	}
	
}



